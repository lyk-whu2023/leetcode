//给你一个整数数组 nums，其中恰好有两个元素只出现一次，其余所有元素均出现两次。
// 找出只出现一次的那两个元素。你可以按 任意顺序 返回答案。
//你必须设计并实现线性时间复杂度的算法且仅使用常量额外空间来解决此问题。

class Solution {
    public int[] singleNumber(int[] nums) {
        // 第一步：计算所有数字的异或值
        // xor 最终结果为两个只出现一次的数字的异或值
        int xor = 0;
        for (int num : nums) {
            xor ^= num;  // 相同的数字异或结果为0，所以最终得到两个不同数字的异或
        }

        // 第二步：找到xor中任意一个为1的位
        // 这个位表示两个只出现一次的数字在该位上不同（一个是0，一个是1）
        // 技巧：xor & (-xor) 可以取到最低位的1
        // 例如：xor = 6 (110)，-xor = -6 (补码: 111...1010)，相与得到 2 (010)
        int mask = xor & (-xor);

        // 第三步：根据这个位将数组分成两组
        // 在该位上为0的为一组，为1的为另一组
        int[] ans = new int[2];
        for (int num : nums) {
            if ((num & mask) == 0) {
                // 该位为0的数字分到第一组
                // 因为相同的数字肯定在同一组，它们会相互抵消
                // 最终剩下的就是那个在该位为0的只出现一次的数字
                ans[0] ^= num;
            } else {
                // 该位为1的数字分到第二组
                // 因为相同的数字肯定在同一组，它们会相互抵消
                // 最终剩下的是那个在该位为1的只出现一次的数字
                ans[1] ^= num;
            }
        }

        return ans;
    }
}